package at.released.weh.gradle.wasm.codegen.witx.parser

import org.antlr.v4.runtime.BaseErrorListener
import org.antlr.v4.runtime.RecognitionException
import org.antlr.v4.runtime.Recognizer

internal object RethrowErrorListener : BaseErrorListener() {
    override fun syntaxError(
        recognizer: Recognizer<*, *>?,
        offendingSymbol: Any?,
        line: Int,
        charPositionInLine: Int,
        msg: String?,
        ex: RecognitionException?,
    ) {
        throw IllegalArgumentException("Could not parse script, error at line $line: $charPositionInLine: $msg")
    }
}
